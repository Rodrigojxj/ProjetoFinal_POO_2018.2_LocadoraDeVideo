    private void listarClientes() {
        listaModelClientes = controllerCliente.getListaClienteController();
        CBXNomeCliente.removeAllItems();
        for (int i = 0; i < listaModelClientes.size(); i++) {
            CBXNomeCliente.addItem(listaModelClientes.get(i).getCliNome());
        }
    }

    private void listarProdutos() {
        listaModelFilme = controllerFilme.getListaFilmeController();
        CBXNomeFilme.removeAllItems();
        for (int i = 0; i < listaModelFilme.size(); i++) {
            CBXNomeFilme.addItem(listaModelFilme.get(i).getFilTitulo());
        }
    }

