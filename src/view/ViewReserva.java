    private void ButAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButAdicionarActionPerformed
        // TODO add your handling code here:
        modelFilme = controllerFilme.getFilmeController(Integer.parseInt(CCodFilm.getText()));
        DefaultTableModel modelo = (DefaultTableModel) tbl_cadastro.getModel();
        int cont = 0;
        for (int i = 0; i < cont; i++) {
            modelo.setNumRows(0);
        }
        modelo.addRow(new Object[]{
        modelFilme.getIdFilme(),
            modelFilme.getFilTitulo(),
            modelFilme.getFilValorReserva()
        });
    }//GEN-LAST:event_ButAdicionarActionPerformed

