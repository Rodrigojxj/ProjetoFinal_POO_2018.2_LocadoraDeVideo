    private void ButSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButSalvarActionPerformed
        try {
            modelUsuario.setIdUsuario(Integer.parseInt(this.CCodigo.getText()));
        } catch (NumberFormatException e) {
        }
        modelUsuario.setUsuNome(CNome.getText());
        modelUsuario.setUsuLogin(CLogin.getText());
        modelUsuario.setUsuSenha(CSenha.getText());
        if (SalvarAlterar.equals("salvar")) {
            if (controllerUsuario.salvarUsuarioController(modelUsuario) > 0) {
                JOptionPane.showMessageDialog(this, "Cadastro Realizado com sucesso!", "ATEN칂츾O", JOptionPane.WARNING_MESSAGE);
                carregarUsuario();
                habilitarDesabilitarCampos(false);
                LimparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao Cadastrar!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            if (controllerUsuario.atualizarUsuarioController(modelUsuario)) {
                JOptionPane.showMessageDialog(this, "Cadastro Alterado com sucesso!", "ATEN칂츾O", JOptionPane.WARNING_MESSAGE);
                carregarUsuario();
                habilitarDesabilitarCampos(false);
                LimparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao Alterar!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_ButSalvarActionPerformed

