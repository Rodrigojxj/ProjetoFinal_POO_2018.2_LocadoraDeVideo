    private void ButAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButAlterarActionPerformed
        int linha = this.tbl_usuario.getSelectedRow();
        try {
            int idUsuario = (int) this.tbl_usuario.getValueAt(linha, 0);
            SalvarAlterar = "alterar";
            // Recupera os dados do banco
            modelUsuario = controllerUsuario.getUsuarioController(idUsuario);
            // Jogar na interface
            this.CCodigo.setText(String.valueOf(modelUsuario.getIdUsuario()));
            this.CNome.setText(modelUsuario.getUsuNome());
            this.CLogin.setText(modelUsuario.getUsuLogin());
            this.CSenha.setText(modelUsuario.getUsuSenha());
            habilitarDesabilitarCampos(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "C칩digo inv치lido ou nenhum registro selecionado!", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ButAlterarActionPerformed

    private void ButExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButExcluirActionPerformed
        int linha = tbl_usuario.getSelectedRow();
        int idUsuario = (int) tbl_usuario.getValueAt(linha, 0);

        if (controllerUsuario.excluirUsuarioController(idUsuario)) {
            JOptionPane.showMessageDialog(this, "Usu치rio Exclu칤do com Sucesso!", "ATEN칂츾O", JOptionPane.WARNING_MESSAGE);
            this.carregarUsuario();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao Excluir o Usu치rio", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ButExcluirActionPerformed

